#Numbers
''' Para entrar el interprete de Python podemos escribir en nuestra consola "py" y esto inciara el interprete
    Podemos usar el interprete como calculadora escribiendo numeros y operadores
    Tambien podemos asignar estos numeros a variables de la siguiente forma '''

num1 = 4
num2 = 5

#Python es un lenguaje de tipado debil por lo cual no es necesario terminar instrucciones con ;
#Podemos operar nuestras variables de la siguiente forma:
res = num1 + num2   #Suma
print(res)

res = num1 - num2   #Resta
print(res)

res = num1 * num2   #Multiplicacion
print(res)

res = num1 / num2   #Division
print(res)

res = num1 % num2   #Modulo
print(res)

res = num1**2       #Potencia
print(res)

